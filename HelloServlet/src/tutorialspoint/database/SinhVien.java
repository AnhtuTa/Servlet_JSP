public class SinhVien {
	String id, name, sex, birthday, faculty, khoa, addr, room;

	public SinhVien(String id, String name, String sex, String birthday, String faculty, String k, String addr,
			String room) {
		super();
		this.id = id;
		this.name = name;
		this.sex = sex;
		this.birthday = birthday;
		this.faculty = faculty;
		this.khoa = k;
		this.addr = addr;
		this.room = room;
	}
	
	
}
